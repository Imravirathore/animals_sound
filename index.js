// Play Audio for Cow

var cow_box = document.getElementById("cow_box")
var cow_audio = document.getElementById("cow_audio"); 



cow_box.addEventListener('click',function(){

   
    cow_audio.play(); 
     

})

cow_box.addEventListener('dblclick',function(){

   
    cow_audio.pause(); 
 

})

